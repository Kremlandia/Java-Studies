package datatypes.alltypes.variables;

/* Tipos de Dados em Java */
/**
 * Os tipos de dados em Java são divididos em grupos de dados numéricos,
 * caractere e booleano.<br><br>
 *
 * <b>Numéricos no campo dos Inteiros</b> - Os tipos de dados numéricos que
 * trabalham no campo dos números inteiros são:
 * <ul>
 * <li><code>byte</code></li>
 * <li><code>short</code></li>
 * <li><code>int</code></li>
 * <li><code>long</code></li>
 * </ul><br>
 *
 * <b>Numéricos no campo dos Reais</b> - Os tipos de dados numéricos que
 * trabalham no campo dos números reais são:
 * <ul>
 * <li><code>float</code></li>
 * <li><code>double</code></li>
 * </ul><br>
 *
 * <b>Caractere</b> - Os tipos de dados caractere são:
 * <ul>
 * <li><code>char</code></li>
 * <li><code>String</code></li>
 * </ul><br>
 *
 * <b>Booleano</b> - O tipo de dado booleano é:
 * <ul>
 * <li><code>boolean</code></li>
 * </ul><br>
 *  Este tipo de dado somente aceita dois valores <code>true</code> ou <code>false</code>.
 *
 * @author eduardo.willian
 *
 */
public class DataType {

    public static Integer getIntegerMaxValue() {
        return Integer.MAX_VALUE;
    }

    public static Byte getByteMaxValue() {
        return Byte.MAX_VALUE;
    }

    public static Short getShortMaxValue() {
        return Short.MAX_VALUE;
    }

    public static Long getLongMaxValue() {
        return Long.MAX_VALUE;
    }

    public static Double getDoubleMaxValue() {
        return Double.MAX_VALUE;
    }

    public static Float getFloatMaxValue() {
        return Float.MAX_VALUE;
    }

    public static Character getCharacterMaxValue() {
        return Character.MAX_VALUE;
    }

}
